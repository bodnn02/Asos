$(".eZ0Gy6f").on("click", function (e) {
    if($(this).attr("aria-expanded")=="false") {
        $(this).attr("aria-expanded","true");
        $(this).parent().find(".oBN6c3V").addClass("EWBhZgg");
    }
    else {
        $(this).attr("aria-expanded","false");
        $(this).parent().find(".oBN6c3V").removeClass("EWBhZgg");
    }
});

